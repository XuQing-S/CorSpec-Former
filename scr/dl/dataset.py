"""PyTorch datasets for LIBS metal classification.

This module supports both preprocessed spectra generated by
``scr/data/data_preprocessing.py`` and tabular features generated by
``scr/data/feature_engineering.py``. Metal labels are always loaded from the
processed data directory.
"""

from __future__ import annotations

import csv
import json
from pathlib import Path

import numpy as np
import torch
from torch.utils.data import DataLoader, Dataset


SplitName = str

SPLITS: tuple[str, ...] = ("train", "val", "test")
EXPECTED_CHANNELS = 1
EXPECTED_SIGNAL_POINTS = 8192
DEFAULT_BATCH_SIZE = 64
FEATURE_FILE_SUFFIXES = {
    "feature": "x_feature",
    "pca": "x_pca",
    "stat": "x_stat",
    "pls_metal": "x_pls_metal",
}


def validate_split(split: str) -> SplitName:
    if split not in SPLITS:
        raise ValueError(f"unsupported split: {split!r}; expected one of {SPLITS}")
    return split


def load_metal_label_names(processed_root: str | Path) -> list[str]:
    """Load metal label names ordered by class index."""
    mapping_path = Path(processed_root) / "label_mapping.json"
    if not mapping_path.exists():
        return []

    with mapping_path.open("r", encoding="utf-8") as file:
        payload = json.load(file)

    metal_mapping = payload.get("metal", {})
    index_to_name = {int(index): str(name) for name, index in metal_mapping.items()}
    return [index_to_name[index] for index in sorted(index_to_name)]


def _load_sample_index(processed_root: Path, split: SplitName) -> list[dict[str, str]]:
    sample_index_path = processed_root / "sample_index.csv"
    if not sample_index_path.exists():
        return []

    rows: list[dict[str, str]] = []
    with sample_index_path.open("r", encoding="utf-8", newline="") as file:
        reader = csv.DictReader(file)
        for row in reader:
            if row.get("split") == split:
                rows.append(dict(row))
    return rows


class LIBSDataset(Dataset):
    """Dataset for one split of preprocessed LIBS metal classification data."""

    def __init__(
        self,
        processed_root: str | Path,
        split: str,
        *,
        load_sample_index: bool = False,
        mmap_mode: str | None = None,
    ) -> None:
        self.processed_root = Path(processed_root)
        self.split = validate_split(split)

        x_path = self.processed_root / f"{self.split}_x.npy"
        y_path = self.processed_root / f"{self.split}_y_metal.npy"
        if not x_path.exists():
            raise FileNotFoundError(f"spectrum tensor not found: {x_path}")
        if not y_path.exists():
            raise FileNotFoundError(f"metal label array not found: {y_path}")

        self.x = np.load(x_path, mmap_mode=mmap_mode)
        self.y = np.load(y_path, mmap_mode=mmap_mode)
        self.sample_index = _load_sample_index(self.processed_root, self.split) if load_sample_index else []
        self.label_names = load_metal_label_names(self.processed_root)

        self._validate_arrays()

    def _validate_arrays(self) -> None:
        if self.x.ndim != 3:
            raise ValueError(f"{self.split}_x.npy must be 3D [N, 1, 8192], got shape {self.x.shape}")
        if self.x.shape[1:] != (EXPECTED_CHANNELS, EXPECTED_SIGNAL_POINTS):
            raise ValueError(
                f"{self.split}_x.npy must have sample shape "
                f"({EXPECTED_CHANNELS}, {EXPECTED_SIGNAL_POINTS}), got {self.x.shape[1:]}"
            )
        if self.y.ndim != 1:
            raise ValueError(f"{self.split}_y_metal.npy must be 1D [N], got shape {self.y.shape}")
        if self.x.shape[0] != self.y.shape[0]:
            raise ValueError(
                f"{self.split}: sample count mismatch between x ({self.x.shape[0]}) and y ({self.y.shape[0]})"
            )
        if self.sample_index and len(self.sample_index) != self.x.shape[0]:
            raise ValueError(
                f"{self.split}: sample_index rows ({len(self.sample_index)}) "
                f"do not match samples ({self.x.shape[0]})"
            )

    def __len__(self) -> int:
        return int(self.x.shape[0])

    def __getitem__(self, index: int) -> tuple[torch.Tensor, torch.Tensor]:
        spectrum = torch.as_tensor(self.x[index], dtype=torch.float32)
        label = torch.as_tensor(int(self.y[index]), dtype=torch.long)
        return spectrum, label

    def get_sample_metadata(self, index: int) -> dict[str, str]:
        """Return the optional row from ``sample_index.csv`` for a sample."""
        if not self.sample_index:
            raise RuntimeError("sample_index.csv was not loaded; set load_sample_index=True")
        return self.sample_index[index]


class LIBSFeatureDataset(Dataset):
    """Dataset for engineered LIBS features and metal labels."""

    def __init__(
        self,
        features_root: str | Path,
        processed_root: str | Path,
        split: str,
        *,
        feature_name: str = "feature",
        load_sample_index: bool = False,
        mmap_mode: str | None = None,
    ) -> None:
        self.features_root = Path(features_root)
        self.processed_root = Path(processed_root)
        self.split = validate_split(split)
        self.feature_name = feature_name

        if feature_name not in FEATURE_FILE_SUFFIXES:
            expected = ", ".join(sorted(FEATURE_FILE_SUFFIXES))
            raise ValueError(f"unsupported feature_name: {feature_name!r}; expected one of {expected}")

        feature_suffix = FEATURE_FILE_SUFFIXES[feature_name]
        x_path = self.features_root / f"{self.split}_{feature_suffix}.npy"
        y_path = self.processed_root / f"{self.split}_y_metal.npy"
        if not x_path.exists():
            raise FileNotFoundError(f"feature matrix not found: {x_path}")
        if not y_path.exists():
            raise FileNotFoundError(f"metal label array not found: {y_path}")

        self.x = np.load(x_path, mmap_mode=mmap_mode)
        self.y = np.load(y_path, mmap_mode=mmap_mode)
        self.sample_index = _load_sample_index(self.processed_root, self.split) if load_sample_index else []
        self.label_names = load_metal_label_names(self.processed_root)

        self._validate_arrays()

    def _validate_arrays(self) -> None:
        if self.x.ndim != 2:
            raise ValueError(f"{self.split}_{self.feature_name} features must be 2D [N, D], got shape {self.x.shape}")
        if self.y.ndim != 1:
            raise ValueError(f"{self.split}_y_metal.npy must be 1D [N], got shape {self.y.shape}")
        if self.x.shape[0] != self.y.shape[0]:
            raise ValueError(
                f"{self.split}: sample count mismatch between features ({self.x.shape[0]}) and y ({self.y.shape[0]})"
            )
        if self.sample_index and len(self.sample_index) != self.x.shape[0]:
            raise ValueError(
                f"{self.split}: sample_index rows ({len(self.sample_index)}) "
                f"do not match samples ({self.x.shape[0]})"
            )

    @property
    def feature_dim(self) -> int:
        return int(self.x.shape[1])

    def __len__(self) -> int:
        return int(self.x.shape[0])

    def __getitem__(self, index: int) -> tuple[torch.Tensor, torch.Tensor]:
        features = torch.as_tensor(self.x[index], dtype=torch.float32)
        label = torch.as_tensor(int(self.y[index]), dtype=torch.long)
        return features, label

    def get_sample_metadata(self, index: int) -> dict[str, str]:
        """Return the optional row from ``sample_index.csv`` for a sample."""
        if not self.sample_index:
            raise RuntimeError("sample_index.csv was not loaded; set load_sample_index=True")
        return self.sample_index[index]


def create_dataloader(
    dataset: Dataset,
    *,
    batch_size: int = DEFAULT_BATCH_SIZE,
    shuffle: bool = False,
    num_workers: int = 0,
    pin_memory: bool | None = None,
    drop_last: bool = False,
) -> DataLoader:
    if pin_memory is None:
        pin_memory = torch.cuda.is_available()
    return DataLoader(
        dataset,
        batch_size=batch_size,
        shuffle=shuffle,
        num_workers=num_workers,
        pin_memory=pin_memory,
        drop_last=drop_last,
    )


def create_metal_dataloaders(
    processed_root: str | Path,
    *,
    batch_size: int = DEFAULT_BATCH_SIZE,
    num_workers: int = 0,
    pin_memory: bool | None = None,
    load_sample_index: bool = False,
) -> dict[SplitName, DataLoader]:
    """Create train/val/test DataLoaders for metal classification."""
    datasets = {
        split: LIBSDataset(
            processed_root,
            split,
            load_sample_index=load_sample_index,
        )
        for split in SPLITS
    }
    return {
        "train": create_dataloader(
            datasets["train"],
            batch_size=batch_size,
            shuffle=True,
            num_workers=num_workers,
            pin_memory=pin_memory,
        ),
        "val": create_dataloader(
            datasets["val"],
            batch_size=batch_size,
            shuffle=False,
            num_workers=num_workers,
            pin_memory=pin_memory,
        ),
        "test": create_dataloader(
            datasets["test"],
            batch_size=batch_size,
            shuffle=False,
            num_workers=num_workers,
            pin_memory=pin_memory,
        ),
    }


def create_feature_metal_dataloaders(
    features_root: str | Path,
    processed_root: str | Path,
    *,
    feature_name: str = "feature",
    batch_size: int = DEFAULT_BATCH_SIZE,
    num_workers: int = 0,
    pin_memory: bool | None = None,
    load_sample_index: bool = False,
) -> dict[SplitName, DataLoader]:
    """Create train/val/test DataLoaders for engineered feature matrices."""
    datasets = {
        split: LIBSFeatureDataset(
            features_root,
            processed_root,
            split,
            feature_name=feature_name,
            load_sample_index=load_sample_index,
        )
        for split in SPLITS
    }
    return {
        "train": create_dataloader(
            datasets["train"],
            batch_size=batch_size,
            shuffle=True,
            num_workers=num_workers,
            pin_memory=pin_memory,
        ),
        "val": create_dataloader(
            datasets["val"],
            batch_size=batch_size,
            shuffle=False,
            num_workers=num_workers,
            pin_memory=pin_memory,
        ),
        "test": create_dataloader(
            datasets["test"],
            batch_size=batch_size,
            shuffle=False,
            num_workers=num_workers,
            pin_memory=pin_memory,
        ),
    }
